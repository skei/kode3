#ifndef kode_point_included
#define kode_point_included
//----------------------------------------------------------------------

class KODE_Point {

//------------------------------
public:
//------------------------------

  float x;
  float y;

//------------------------------
public:
//------------------------------

  KODE_Point() {
    x = 0;
    y = 0;
  };

  KODE_Point(float v) {
    x = v;
    y = v;
  };

  KODE_Point(float ax, float ay) {
    x = ax;
    y = ay;
  };

//------------------------------
public:
//------------------------------

};

//----------------------------------------------------------------------
#endif

